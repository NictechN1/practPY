"""
ДИАГРАММА СОСТОЯНИЙ
Состояния контакта в системе
"""

class StateDiagram:
    def __init__(self):
        self.entity = "Контакт"
        self.states = [
            "НЕ_СОЗДАН",
            "СОЗДАН",
            "РЕДАКТИРУЕТСЯ", 
            "АРХИВИРОВАН",
            "УДАЛЕН"
        ]
        self.transitions = [
            "НЕ_СОЗДАН → СОЗДАН: Пользователь добавляет контакт",
            "СОЗДАН → РЕДАКТИРУЕТСЯ: Пользователь изменяет данные",
            "РЕДАКТИРУЕТСЯ → СОЗДАН: Сохранение изменений",
            "СОЗДАН → АРХИВИРОВАН: Пользователь архивирует контакт",
            "АРХИВИРОВАН → СОЗДАН: Восстановление из архива",
            "СОЗДАН → УДАЛЕН: Окончательное удаление",
            "АРХИВИРОВАН → УДАЛЕН: Окончательное удаление"
        ]
    
    def display(self):
        print(f"ДИАГРАММА СОСТОЯНИЙ для: {self.entity}")
        print("\nСостояния:")
        for state in self.states:
            print(f"- {state}")
        print("\nПереходы между состояниями:")
        for transition in self.transitions:
            print(f"- {transition}")

# Создаем и отображаем диаграмму
state_diagram = StateDiagram()
state_diagram.display()