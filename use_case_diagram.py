"""
ДИАГРАММА ВАРИАНТОВ ИСПОЛЬЗОВАНИЯ
АИС "Телефонный справочник"

Акторы:
- Пользователь

Варианты использования:
1. Добавить контакт
2. Найти контакт
3. Редактировать контакт
4. Удалить контакт
5. Просмотреть все контакты
6. Экспорт контактов
7. Импорт контактов

Связи:
- Пользователь может выполнять все варианты использования
"""

class UseCaseDiagram:
    def __init__(self):
        self.actor = "Пользователь"
        self.use_cases = [
            "Добавить контакт",
            "Найти контакт", 
            "Редактировать контакт",
            "Удалить контакт",
            "Просмотреть все контакты",
            "Экспорт контактов",
            "Импорт контактов"
        ]
    
    def display(self):
        print(f"Актор: {self.actor}")
        print("Варианты использования:")
        for uc in self.use_cases:
            print(f"- {uc}")

# Создаем и отображаем диаграмму
diagram = UseCaseDiagram()
diagram.display()